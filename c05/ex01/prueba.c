#include <unistd.h>
#include <stdio.h>

int	ft_recursive_factorial(int nb)
{
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	else
		return (nb * ft_recursive_factorial(nb - 1));
}

int	main(void)
{
	int	nb;
	int	ret;

	nb = 6;
	ret = ft_recursive_factorial(nb);
	printf("%d\n", ret);
}
