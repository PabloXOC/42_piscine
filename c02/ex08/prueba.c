#include <unistd.h>
#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	counter;

	counter = 0;
	while (str[counter])
	{
		if (str[counter] >= 'A' && str[counter] <= 'Z')
			str[counter] = str[counter] + 32;
		counter++;
	}
	return (str);
}

int	main(void)
{
	char	str[] = "hola9898989FGBJDFGBJ";

	ft_strlowcase(str);
	printf("%s", str);
}
